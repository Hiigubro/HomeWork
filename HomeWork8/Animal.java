package HomeWork8;

/**
 * 2. Написать класс Animal:
 *  2.1. Число age - возраст
 *  2.2. Строка nick - кличка
 */

public class Animal {
    private int age;
    private String nick;

    public Animal(Integer age, String nick) {
        this.nick = nick;
        this.nick = nick;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }
}
